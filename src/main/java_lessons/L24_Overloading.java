public class L24_Overloading {

    public static void main(String[] args) {

        // Method Overloading
        String name = "Laptop";
        System.out.println(name.indexOf(1));
        System.out.println(name.indexOf("a"));
        System.out.println(name.indexOf(1,4));
        System.out.println(name.indexOf("a",4));
    }
}
