import java.util.ArrayList;

public class L2_Variables {
    // Primitive Types
    // Primitive data type will be written as lowercase
    // Primitive data type has only values
    // Primitive data created by only Java
    // All Primitive data types has different size and range of values
    // All Primitive data types stores on Stack memory
    // All Primitive data types stores on Stack memory in the container of the variable
    byte number2 = 127;         // 8 bit - 1 byte, from -128 to 127
    short number3 = 32767;      // 16 bit - 2 byte, from -32768 to 32767
    int number1 = 2147483647;   // 32 bit - 4 byte, from -2147483648 to 2147483647
    float number5 = 10.0f;      // 32 bit - 4 byte, from -3.4028235E38 to 3.4028235E45 (7 characters after decimal point)
    double number6 = 10.0;      // 64 bit - 8 byte, from -1.7976931348623157E308 to 1.797693134862 ()
    long number4 = 10;          // 64 bit - 8 byte, from -9223372036854775808 to 92233720368547758
    char letter = 'A';          // 16 bit - 2 byte, all Unicode characters
    boolean isTrue = true;      // 8 bit - 1 byte, from true to false
    // Non-Primitive Types
    // Non-Primitive data types will be written as Capitalized
    // Non-Primitive data types has methods
    // Non-Primitive data types can be created by user
    // All Non-Primitive data types has same value but different address (Address are also stored on Stack memory   )
    // All Non-Primitive data types stores on Heap memory
    // All Non-Primitive data types stores on Heap memory in the object of the variable
    String name = "Mesut";      // 32 bit, from 0 to 4294967295
    ArrayList<String> arrayList = new ArrayList<String>();

}
